/* USER CODE BEGIN 0 */
uint8_t GenerateRandomNumber(uint8_t min, uint8_t max)
{
    uint8_t random_number = 0;
    random_number         = HAL_RNG_GetRandomNumber(&hrng);
    return (random_number % (max - min + 1)) + min;
}
uint8_t CalculateKey(uint8_t seed)
{
    uint8_t key = 0;
    for (int i = 0; i < 8; i++) {
        key += (seed >> i) & 1;
    }
    return key;
}
void PrepareMessage()
{
    for (int i = 0; i < 8; i++) {
        Node_TxData[i] = 0x55;
    }
}
/* Interrupt */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
    // HAL_GPIO_TogglePin(LD1_GPIO_Port, LD1_Pin);
    HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &Node_RxHeader, Node_RxData);
    uint8_t data_length = Node_RxData[0] & 0x0F;
    for (int i = 0; i < data_length; i++) {
        message[i] = Node_RxData[i + 1];
    }
    new_message = true;
    if (unlocked) { // extend the time if receive a message
        __HAL_TIM_SET_COUNTER(&htim2, 0);
    }
}
