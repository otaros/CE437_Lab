                seed = GenerateRandomNumber(0, 255); // generate random seed
                PrepareMessage();
                Node_TxData[0] = 0x03;
                Node_TxData[1] = 0x27 + 0x40;
                Node_TxData[2] = 0x01;
                Node_TxData[3] = seed;
                new_message    = false;
